Num = int(input("Escreva um dia da semana(1 a 7): "))
if Num == 1:
    print("Domingo")
elif Num == 2:
    print("Segunda")
elif Num == 3:
    print("Terça")
elif Num == 4:
    print("Quarta")
elif Num == 5:
    print("Quinta")
elif Num == 6:
    print("Sexta")
elif Num == 7:
    print("Sabado")
else:
    print("Dia da semana inválido")
